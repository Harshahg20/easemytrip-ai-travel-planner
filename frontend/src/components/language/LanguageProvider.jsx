export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState("english");

  // Load language preference from localStorage on mount
  useEffect(() => {
    const savedLanguage = localStorage.getItem("tripora_language");
    if (savedLanguage && translations[savedLanguage]) {
      setCurrentLanguage(savedLanguage);
    }
  }, []);

  // Save language preference to localStorage when changed
  useEffect(() => {
    localStorage.setItem("tripora_language", currentLanguage);
  }, [currentLanguage]);

  const changeLanguage = (language) => {
    if (translations[language]) {
      setCurrentLanguage(language);
    }
  };

  const t = (key, params = {}) => {
    const keys = key.split(".");
    let primaryLang = translations[currentLanguage];
    let fallbackLang = translations.english;

    let value = primaryLang;
    for (const k of keys) {
      value = value?.[k];
    }

    if (typeof value === "undefined") {
      // Fallback to English if translation not found in current language
      value = fallbackLang;
      for (const k of keys) {
        value = value?.[k];
      }
    }

    if (typeof value === "string" && params) {
      // Replace placeholders like {current}, {total}, etc.
      return value.replace(
        /\{(\w+)\}/g,
        (match, param) => params[param] || match
      );
    }

    return value || key;
  };

  const value = {
    currentLanguage,
    changeLanguage,
    t,
    translations: translations[currentLanguage],
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};
